//Arithemetic 
let a = 10, b = 3;
console.log(a + b); 
console.log(a * b); 
console.log(a / b); 
console.log(a % b);
console.log(2 ** 4); 


//Assignment Operators
let x = 5;
x += 2; 
x -= 1; 
x *= 2;
x /= 4; 
console.log(x);


// Comparison Operators
console.log(5 == "5");  
console.log(5 === "5");
console.log(10 > 8); 
console.log(10 != 9);


//Logical Operators

let age = 25;
console.log(age > 18 && age < 30); 
console.log(age > 30 || age === 25);
console.log(!(age === 25)); 

// Ternary Operator (Conditional)
let score = 75;
let result = score >= 50 ? "Pass" : "Fail";
console.log(result);



